import { randomBytes } from 'node:crypto';

/* ---------------------------------------------------------------------- */

export function randomId(bytes = 32): string {
    const b = randomBytes(bytes);
    return b
        .toString('base64')
        .replace(/\+/g, '-')
        .replace(/\//g, '_')
        .replace(/=+$/g, '');
}

